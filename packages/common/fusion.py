"""Reciprocal Rank Fusion: слияние результатов нескольких модальностей поиска по рангу.

В системе два способа поиска — плотный векторный (Qdrant, косинусное сходство) и
разреженный keyword-поиск (в духе BM25). Их скоры лежат на совершенно разных,
ненормированных шкалах — сложить или усреднить их напрямую нельзя. RRF решает это,
выбрасывая сырые скоры и сливая списки по *позиции в ранге*: вклад чанка из одного списка
равен ``1 / (k + rank + 1)`` (rank считается с нуля), итоговый скор — сумма по всем спискам,
где чанк встретился. Сглаживающая константа k (по умолчанию 60) не даёт первому месту
доминировать слишком сильно — то же значение, что использует сам Qdrant в своём серверном
Query API при работе с prefetch-ветками.

Этот модуль воспроизводит ту же арифметику на клиенте — так итоговое ранжирование можно
проверить независимо от живого Qdrant. Математика идентична: вывод RRF у Qdrant и вывод
reciprocal_rank_fusion совпадают на одинаковых входных данных.

Источник: Cormack, Clarke & Buettcher, «Reciprocal Rank Fusion outperforms Condorcet and
individual Rank Learning Methods», SIGIR 2009.

Здесь же exact_name_boost — детерминированный постпроцессор, который жёстко поднимает в
начало списка кандидатов, чьё имя символа точно совпадает с токеном запроса, сохраняя
относительный порядок внутри каждой из двух групп (поднятые, затем остальные). Вспомогательные
функции для него — query_tokens и symbol_name_for.
"""

from __future__ import annotations

import os
import re
from collections.abc import Callable, Sequence

from common.stores import SearchHit

_TOKEN_RE: re.Pattern[str] = re.compile(r"[^0-9a-z_]+")
"""Разбивает запрос на токены-идентификаторы: любая последовательность символов, не входящих
в [0-9a-z_], становится разделителем. Подчёркивание нарочно остаётся частью токена, чтобы
символ вроде verify_password не рассыпался на два токена (в отличие от word-piece
токенизатора эмбеддера, который режет по '_')."""


def reciprocal_rank_fusion(
    rankings: Sequence[Sequence[str]],
    *,
    k: int = 60,
    limit: int | None = None,
) -> list[SearchHit]:
    """Слить несколько ранжированных списков chunk_id в один по RRF-скору.

    rankings — по одному ранжированному списку chunk_id на каждую модальность, лучшие
    элементы первыми (индекс 0). Списков может быть сколько угодно; типичный случай —
    список плотного векторного поиска и список разреженного BM25.

    k — сглаживающая константа RRF, положительное целое (по умолчанию 60 — общепринятое
    значение у Elasticsearch, Qdrant и в самой статье SIGIR). Чем больше k, тем более пологая
    кривая скора — верхний ранг значит относительно меньше по сравнению с нижними.

    limit — если задан, обрезает отсортированный результат до первых limit элементов;
    limit <= 0 даёт пустой список (как и лимит у search в векторном хранилище — это просто
    срез [:limit], так что обычная семантика среза Python работает и для отрицательных значений).

    Возвращает по одному SearchHit на каждый уникальный chunk_id, отсортированный по убыванию
    RRF-скора, ничьи разбиваются по возрастанию chunk_id для детерминизма.

    Возвращает ValueError, если k <= 0.

    Формула скора: для chunk_id, встретившегося на позиции rank (с нуля) в списке, его вклад
    — ``1.0 / (k + rank + 1)``. Плюс единица даёт первому месту вклад ``1 / (k + 1)`` —
    классический 1-based RRF, выраженный через 0-based индексацию. Вклады из всех списков,
    где встретился id, суммируются; если id повторяется внутри одного списка, считается только
    первое (наименьшее) вхождение — без задвоения внутри списка.

    Пустой rankings или списки из одних пустых списков → пустой результат.
    """
    if k <= 0:
        raise ValueError(f"k must be a positive integer, got k={k!r}")

    scores: dict[str, float] = {}
    for ranking in rankings:
        seen: set[str] = set()
        for rank, chunk_id in enumerate(ranking):
            if chunk_id in seen:
                continue
            seen.add(chunk_id)
            scores[chunk_id] = scores.get(chunk_id, 0.0) + 1.0 / (k + rank + 1)

    hits = [SearchHit(chunk_id=cid, score=score) for cid, score in scores.items()]
    hits.sort(key=lambda h: (-h.score, h.chunk_id))

    if limit is not None:
        hits = hits[:limit]

    return hits


def query_tokens(query: str) -> frozenset[str]:
    """Множество токенов-идентификаторов из query, приведённых к нижнему регистру.

    Сначала весь запрос приводится к нижнему регистру, затем режется по любой
    последовательности символов вне [0-9a-z_]. Подчёркивание остаётся частью токена, так
    что verify_password не разваливается на verify и password. Пустые строки, оставшиеся
    после разбиения (например, из-за ведущего разделителя), отбрасываются.

    query — запрос на естественном языке или по имени символа кода, например
    "How does verify_password work?" или просто "verify_password".

    Возвращает неизменяемое множество токенов в нижнем регистре; пустое, если query пуст
    или состоит только из символов-разделителей.

    >>> query_tokens("verify_password foo")
    frozenset({'verify_password', 'foo'})
    >>> query_tokens("  ")
    frozenset()
    """
    return frozenset(t for t in _TOKEN_RE.split(query.lower()) if t)


def symbol_name_for(path: str) -> str:
    """Вывести имя символа кандидата-чанка из пути к файлу.

    Имя символа — это basename файла без последнего расширения, то же самое, что
    pathlib.Path(path).stem, но через os.path, чтобы не тянуть лишний импорт.

    Регистр сохраняется как есть: приведение к нижнему регистру для сравнения — забота
    вызывающей стороны (см. exact_name_boost).

    path — путь к файлу, относительный или абсолютный, например "src/auth.py".

    Возвращает основу basename, например "auth" для "src/auth.py" или "Makefile" для
    "Makefile" (нет расширения — возвращается весь basename).

    >>> symbol_name_for("src/auth.py")
    'auth'
    >>> symbol_name_for("a/b/verify_password.py")
    'verify_password'
    >>> symbol_name_for("Makefile")
    'Makefile'
    """
    basename = os.path.basename(path)
    stem, _ = os.path.splitext(basename)
    return stem


def exact_name_boost[T](
    ranked: Sequence[T],
    query: str,
    name_of: Callable[[T], str],
) -> list[T]:
    """Жёстко поднять в начало списка элементы, чьё имя символа точно совпало с токеном запроса.

    Это детерминированное перемещение, а не подкрутка скора. Кандидаты, чьё имя символа
    (в нижнем регистре) входит в множество токенов query (см. query_tokens), переносятся
    в начало возвращаемого списка. Остальные кандидаты идут следом в исходном относительном
    порядке; внутри поднятой группы порядок совпавших элементов тоже сохраняется.

    Функция обобщена по типу элемента T — работает на любой ранжированной последовательности,
    если вызывающая сторона передаёт name_of, возвращающий имя символа для элемента. Для
    настоящего результата поиска типичный аксессор — lambda hit: symbol_name_for(hit.path),
    но подходят и обычные строки, и датаклассы (см. юнит-тесты).

    ranked — входная ранжированная последовательность, лучшие первыми; подойдёт любой Sequence
    (список, кортеж, …), исходный контейнер не мутируется.
    query — пользовательский запрос на естественном языке или по имени символа; токенизируется
    внутри через query_tokens.
    name_of — функция от одного аргумента, возвращающая имя символа элемента.

    Возвращает новый список: сначала поднятые элементы (в своём относительном порядке), затем
    остальные (тоже в своём относительном порядке).

    Безопасные случаи без эффекта: если в query нет токенов-идентификаторов (пустая строка
    или только разделители) или ни один элемент не совпал ни с одним токеном — функция просто
    возвращает копию ranked в исходном порядке, вызов всегда безопасен.

    Регистр: query_tokens приводит запрос к нижнему регистру, сравнение идёт по
    name_of(item).lower(), так что "Auth" совпадёт с токеном "auth".

    >>> exact_name_boost(["auth.py", "models.py", "views.py"], "auth", lambda p: p.split(".")[0])
    ['auth.py', 'models.py', 'views.py']
    """
    tokens = query_tokens(query)
    if not tokens:
        return list(ranked)

    boosted: list[T] = []
    rest: list[T] = []
    for item in ranked:
        if name_of(item).lower() in tokens:
            boosted.append(item)
        else:
            rest.append(item)

    return boosted + rest
